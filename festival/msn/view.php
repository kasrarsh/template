                <p class="">Scarlett Johansson has come out strongly in defence of Woody Allen, saying she “loved” the
                    film-maker and “believed” he is innocent of the abuse accusations made by his adopted daughter Dylan
                    Farrow</p>
                <p class="">In an interview with the Hollywood Reporter, Johansson was asked how she felt about Allen,
                    who has directed her in three films: Match Point, Scoop and Vicky Cristina Barcelona. She said: “I
                    love Woody. I believe him, and I would work with him any time.”</p>
                <p class="">ohansson’s position is in clear opposition to actors such as Timothée Chalamet, Greta Gerwig
                    and Colin Firth, all of whom expressed regret at working with Allen after the re-emergence of
                    Farrow’s allegation of sexual abuse against him. Allen has always denied the allegations, which have
                    been investigated twice by authorities and no charges brought. Other actors such as Javier Bardem
                    and Anjelica Huston have proclaimed their support.</p>
                <img src="https://img-s-msn-com.akamaized.net/tenant/amp/entityid/BBQ88Xl.img?h=618&w=799&m=6&q=60&o=f&l=f&x=1921&y=908">
                <p class="">Johansson added: “I see Woody whenever I can, and I have had a lot of conversations with him
                    about it. I have been very direct with him, and he’s very direct with me. He maintains his
                    innocence, and I believe him.”

                    Allen is currently in a legal dispute with Amazon, the backer of his most recently completed film A
                    Rainy Day in New York. Amazon terminated a four-film agreement with him, citing his comments about
                    the #MeToo movement. Johansson, a founding member of the Time’s Up campaign, acknowledged others’
                    concerns abut Allen. “It’s hard because it’s a time where people are very fired up, and
                    understandably,” she said. “Things needed to be stirred up, and so people have a lot of passion and
                    a lot of strong feelings and are angry, rightfully so. It’s an intense time.”</p>
